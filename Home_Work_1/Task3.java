package Home_Work_1;

public class Task3 {
    public static void main(String[] args) {
        //модули числа
        System.out.println(Math.abs(-2134));//2134
        System.out.println(Math.abs(86.546));//86.546

        // Math.random - сгенерируем и выведем на экран рандомное число от 0 до 100
        double x = Math.random() * 100;
        System.out.println(x);//5.5591407009768945

        //Math.round() - округлим число до ближайшего целого
        System.out.println(Math.round(5.7415646F)); //round(float a) = 6
        System.out.println(Math.round(2.54546798126D)); //round(double a) =3
    }
}














